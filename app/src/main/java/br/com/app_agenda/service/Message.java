package br.com.app_agenda.service;

public class Message {

    private String msg;
    private boolean auth;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public boolean getAuth() {
        return auth;
    }

    public void setAuth() {
        this.auth = auth;
    }
}
